#!/usr/bin/env node
/**
 * Simple Conventional Commits validator.
 * Format: <type>(<scope>): <subject>
 */
import { readFileSync } from 'node:fs';

const TYPES = [
  'feat',
  'fix',
  'docs',
  'style',
  'refactor',
  'perf',
  'test',
  'chore',
  'build',
  'ci',
  'revert',
];
const PATTERN = new RegExp(`^(${TYPES.join('|')})(\\([a-z0-9,\\-\\s]+\\))?!?:\\s.+`);

const msgFile = process.argv[2];
if (!msgFile) {
  console.error('commit-msg: no commit message file passed');
  process.exit(1);
}

const msg = readFileSync(msgFile, 'utf8').split('\n')[0].trim();

// Allow merge commits and revert commits generated by git
if (msg.startsWith('Merge ') || msg.startsWith('Revert ')) process.exit(0);

if (!PATTERN.test(msg)) {
  console.error('');
  console.error('✖ Commit message does not follow Conventional Commits format.');
  console.error('');
  console.error('  Expected: <type>(<scope>): <subject>');
  console.error(`  Types:    ${TYPES.join(', ')}`);
  console.error('  Example:  feat(rules): implement chain resolution');
  console.error('');
  console.error(`  Got: ${msg}`);
  console.error('');
  process.exit(1);
}
